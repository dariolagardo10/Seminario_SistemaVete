    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        if (this.bandera.equals("editar")) { // Verifica si la bandera indica que se debe editar un artÃ­culo
            // Obtiene los valores de los campos de texto
            String nombre = txtnom.getText();
            int stock = Integer.parseInt(txtstock1.getText());
            double precio = Double.parseDouble(txtprecio.getText());

            // Verifica nuevamente si la bandera indica que se debe editar
            if (this.bandera.equals("editar")) {
                // Verifica que haya una fila seleccionada en la tabla de usuarios
                if (tblusuarios.getSelectedRow() > -1 && tblusuarios.getSelectedRowCount() == 1) {
                    // Obtiene el ID del artÃ­culo seleccionado
                    int seleccionado = (int) tblusuarios.getValueAt(tblusuarios.getSelectedRow(), 0);
                    // Busca el artÃ­culo por su ID
                    Articulo u = ctrl.buscarArtxId(seleccionado);
                    // Actualiza los datos del artÃ­culo con los valores obtenidos de los campos de texto
                    u.setNombre(nombre);
                    u.setStockActual(stock);
                    u.setPrecioActual(precio);
                    try {
                        // Edita el artÃ­culo en la base de datos
                        ctrl.editarArt(u);
                    } catch (Exception ex) {
                        // Maneja cualquier excepciÃ³n que ocurra durante la ediciÃ³n
                        Logger.getLogger(FrmUsuario.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        } else {
            // Muestra un mensaje indicando que se debe seleccionar el botÃ³n de editar
            JOptionPane.showMessageDialog(null, "Debe seleccionar el botÃ³n de editar");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

