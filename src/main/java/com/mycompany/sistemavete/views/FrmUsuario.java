    public void CargarT(Usuario u){
    DefaultTableModel tabla=new DefaultTableModel();
    String cabecera[] = {"IdUsuario","Nombre","Usuario"};
    tabla.setColumnIdentifiers(cabecera);
    Object fila[]=new Object[tabla.getColumnCount()];
    if(u!=null){
      
            fila[0]=u.getId_Usu();
            fila[1]=u.getNombre();
            fila[2]=u.getUsuario();
           
            
            tabla.addRow(fila);
       
           
        
        tblusuarios.setModel(tabla);
    }
     }
